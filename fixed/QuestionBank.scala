  // Lazy functions (lecture 10)
  // beyro7 masalan 3ala  lazy val easyQuestions we beye3mal filter lel eagabat el easy we beye3maleha save
  // ba3d keda law nadet 3aleha tany beytala3 el egaba 3ala tool me4 mehtag ye3mal filter tany

  lazy val easyTriviaQuestions: List[TriviaQuestion] = {
    TriviaQuestions.filter(_.difficulty == "easy")
  }
  lazy val hardTriviaQuestions: List[TriviaQuestion] = {
    TriviaQuestions.filter(_.difficulty == "hard")
  }
  lazy val questionsByEra: Map[String, List[TriviaQuestion]] = {
    TriviaQuestions.groupBy(_.era)
  }
  lazy val questionsByTopic: Map[String, List[TriviaQuestion]] = {
    TriviaQuestions.groupBy(_.topic)
  }

  // currying ( lecture 9)
  // filterQuestions byea5od 2awal haga el era we el topic
  //ba3d keda howa beysabet el era we beyefdal ye8ayar fe el topic
  //we yetala3 el 2as2ela ely 3ala el topic ely enta sa2al 3aleh

  def filterQuestions(era: String)(topic: String): List[TriviaQuestion] = {
    TriviaQuestions.filter(q => q.era == era && q.topic == topic)
  }

  //mesabet el era we beyefdal yes2al howa 3ayez anhy topic

  val ancientQuestions: String => List[TriviaQuestion]     = filterQuestions("ancient")
  val medievalQuestions: String => List[TriviaQuestion]    = filterQuestions("medieval")
  val modernQuestions: String => List[TriviaQuestion]      = filterQuestions("modern")
  val earlyModernQuestions: String => List[TriviaQuestion] = filterQuestions("earlymodern")

  // closures( lecture 9)
  //  difficultyFilter beya5od difficulty we beygeblak kol el 2as2ela el e5tart el difficulty beta3etha

  def difficultyFilter(difficulty: String): List[TriviaQuestion] => List[TriviaQuestion] = {
    questionList => questionList.filter(_.difficulty == difficulty)
  }
  val onlyEasy: List[TriviaQuestion] => List[TriviaQuestion]   = difficultyFilter("easy")
  val onlyMedium: List[TriviaQuestion] => List[TriviaQuestion] = difficultyFilter("medium")
  val onlyHard: List[TriviaQuestion] => List[TriviaQuestion]   = difficultyFilter("hard")
}